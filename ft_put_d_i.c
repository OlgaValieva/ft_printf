#include "ft_printf.h"

void	ft_put_d_i(int i, int *res)
{
	char	c;

	if (i < 0)
	{
		ft_put_c('-', res);
		c = '0' - (i % 10);
		*res += 1;
		if (i / -10 != 0)
			ft_put_d_i((i / -10), res);
		write(1, &c, 1);
	}
	else
	{
		c = i % 10 + '0';
		*res += 1;
		if (i / 10 != 0)
			ft_put_d_i(i / 10, res);
		write(1, &c, 1);
	}
}
